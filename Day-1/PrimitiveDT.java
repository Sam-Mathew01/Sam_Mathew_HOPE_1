public class PrimitiveDT {
    public static void main(String[] args) {
        byte a=-128;
        byte b=127;
        System.out.println("The Range of byte is:"+a+","+b);
        short sh1=-32768;
        short sh2=32767;
        System.out.println("The Range of short is:"+sh1+","+sh2);
        int num1=-2147483648;
        int num2=2147483647;
        System.out.println("The Range of int is:"+num1+","+num2);
        long l1=250L;
        System.out.println(l1);
        boolean bool1=true;
        boolean bool2=false;
        System.out.println("Values of Boolean is:"+bool1+","+bool2);
        float f=10.5f;
        System.out.println(f);
        double d=200000.2599;
        System.out.println(d);

    }
}
